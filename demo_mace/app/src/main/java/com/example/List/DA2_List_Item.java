package com.example.List;

// pop시 나오는 일별 상세 내용
public class DA2_List_Item {
    private String strExercise;
    private String ExerciseCount;
    private String strText;

    public String getStrExercise() {
        return strExercise;
    }

    public String getExerciseCount() {
        return ExerciseCount;
    }

    public String getStrText() {
        return strText;
    }

    public void setStrExercise(String strExercise) {
        this.strExercise = strExercise;
    }

    public void setExerciseCount(String exerciseCount) {
        ExerciseCount = exerciseCount;
    }

    public void setStrText(String strText) {
        this.strText = strText;
    }
}
