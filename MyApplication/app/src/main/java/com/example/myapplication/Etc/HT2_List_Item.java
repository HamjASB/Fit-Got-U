package com.example.myapplication.Etc;

public class HT2_List_Item {
    private String image;
    private String name;
    private String desc;

    public HT2_List_Item(String image, String name, String desc) {
        this.image = image;
        this.name = name;
        this.desc = desc;
    }

    public String getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

    public String getDesc() {
        return desc;
    }
}
